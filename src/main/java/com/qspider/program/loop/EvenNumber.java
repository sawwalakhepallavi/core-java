package com.qspider.program.loop;
//write a java program to print the even number 1 to 10
public class EvenNumber {
    public static void main(String[] args) {
        for(int i=1;i<=10;i++){
            if(i%2==0){
                System.out.println(i +" number is even");
            }
        }
    }
}
